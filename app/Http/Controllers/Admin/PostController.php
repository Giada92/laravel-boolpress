        $slugVerifica = Post::where('slug', $slug)->first();
        //dd($slugVerifica);

        $slug_base = $slug;
        $count = 1;

        while($slugVerifica != null){
            
            $slug = $slug_base . "-" . $count;

            $slugVerifica = Post::where('slug', $slug)->first();
            $count++;
        }

        return $slug;
        $data['slug'] = $slug;
        

        $newPost->fill($data);
        
        $newPost->save();
        return redirect()->route('admin.posts.show', $newPost->id);

    }

